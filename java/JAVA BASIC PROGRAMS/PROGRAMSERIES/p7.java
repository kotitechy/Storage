//p-7. SWAPING OF TWO NUMBERS

public class p7 {
    public static void main(String[] args) {
        int a = 10 ,b = 20,c ;
        System.out.println("VALUES BEFORE SWAPING : \n A : " + a + "   B : " + b );
        c = a;
        a = b;
        b = c;
        
        System.out.println("VALUES AFTER  SWAPING : \n A : " + a + "    B : " + b);
    }
}
