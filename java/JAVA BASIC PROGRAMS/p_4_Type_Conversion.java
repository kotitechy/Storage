//Type conversion methods in java
/*
we can convert some variables from one to another datate using type conversion techniqes*/
class p_4_Type_Conversion{
public static void main(String [] args){
//===>> here converting float to int
float pi = 3.14f;
int num_pi = (int)pi;   //===>> here pi float value is now convered to integer 
System.out.println("The pi value is " + num_pi);
System.out.println("The num_pi: " + (float)num_pi);  //===>> Converting the int to float again		
}
}

