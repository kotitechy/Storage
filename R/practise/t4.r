# program- 4 all dtypes
a = 4 # numeric
b = 3L # integer
c = a > b  # logical
d = 3+4i # complex
e = "@Shiva" # character
# check class 
print(class(a))
print(class(b))
print(class(c))
print(class(d))
print(class(e))
# check type of variable
print(typeof(a))
print(typeof(b))
print(typeof(c))
print(typeof(d))
print(typeof(e))
# check dtype of objects
print(is.numeric(a))
print(is.integer(a))
print(is.logical(a))
print(is.complex(b))
print(is.character(a))


